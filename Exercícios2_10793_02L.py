# Exercício de Variáveis e Operações Matemáticas
# As duas primeiras linhas pedem que o utilizador introduza os números
print("\n")
Primeiro_numero = input("Introduza um número inteiro, pf: ")
print("\n")
Segundo_numero = input("Introduza outro número inteiro, pf: ")
print("\n")
# Estas linhas dão o resultado das operações integradas numa frase explicativa
print("A soma dos dois números dá " + str(int(Primeiro_numero)+int(Segundo_numero)))
print("\n")
print("A subtração do primeiro número pelo segundo dá " + str(int(Primeiro_numero)-int(Segundo_numero)))
print("\n")
print("A multiplicação dos dois números dá " + str(int(Primeiro_numero)*int(Segundo_numero)))
print("\n")
print("A divisão do primeiro número pelo segundo dá " + str(int(Primeiro_numero)/int(Segundo_numero)))
print("\n")